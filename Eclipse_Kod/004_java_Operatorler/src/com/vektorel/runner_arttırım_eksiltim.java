package com.vektorel;

public class runner_arttýrým_eksiltim {

	public static void main(String[] args) {
		
		int sayi = 5;
		sayi = sayi+1;
		System.out.println("sayi: "+sayi);
		// ++ operatörü bir sayýnýn deðerini 1 arttýrmak için kullanýlýr
		sayi++;
		System.out.println("sayi: "+sayi);
		// -- operatörü bir sayýnýn deðerini 1 eksiltmek için
		sayi--;
		System.out.println("sayi: "+sayi);
		
		
	}

}
